// Netlify serverless function — proxies requests to the Anthropic API.
// The API key lives here, server-side, as an environment variable —
// never in the browser code. Set ANTHROPIC_API_KEY in Netlify's
// Site settings > Environment variables before deploying.

exports.handler = async function (event) {
  if (event.httpMethod !== 'POST') {
    return { statusCode: 405, body: 'Method Not Allowed' };
  }

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    return {
      statusCode: 500,
      body: JSON.stringify({ error: { message: 'Server is missing ANTHROPIC_API_KEY. Set it in Netlify > Site settings > Environment variables.' } })
    };
  }

  try {
    const payload = JSON.parse(event.body);

    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify(payload)
    });

    const data = await response.json();

    return {
      statusCode: response.status,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(data)
    };
  } catch (err) {
    return {
      statusCode: 500,
      body: JSON.stringify({ error: { message: err.message || 'Proxy error' } })
    };
  }
};
